    var images = [ "images/2.jpg","images/3.jpg","images/4.jpg","images/5.jpg","images/6.png","images/8.jpg","images/9.jpg" ,"images/Intell1.png"];
  var currentIndex = 0;
  var imageElement = $("#myImage"); 

  function changeImage() {
    // Add fading effect using jQuery
    imageElement.fadeOut(1000, function() {
      imageElement.attr("src", images[currentIndex]);
      currentIndex++;
      if (currentIndex >= images.length) {
        currentIndex = 0;
      }
      imageElement.fadeIn(1000); 
    });
  }

  setInterval(changeImage, 2500);
